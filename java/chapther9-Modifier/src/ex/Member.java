package ex;

public class Member {

	private String name;
	private String id;

	// getter / setter 메서드
	// 외부에서 변환 / 값을 받아서 변수에 변환
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
	
}
